document.addEventListener("DOMContentLoaded", () => {
    let canvas = document.getElementById("videoCanvas");
    const loader = document.getElementById("loader");
    
    // Most standard AA deployments run 1080p or 720p. Must match Rust backend resolution requested from phone.
    const AA_WIDTH = 1920;
    const AA_HEIGHT = 1080;
    
    // -----------------------------------------------------------------
    // 1. BROADWAY H.264 NAL DECODER SETUP
    // -----------------------------------------------------------------
    let player = null;
    try {
        // Player is provided by Broadway.js bundle
        player = new Player({
            useWorker: true,
            webgl: true,
            size: { width: AA_WIDTH, height: AA_HEIGHT }
        });
        
        // Replace our placeholder canvas with the one generated by Broadway 
        canvas.replaceWith(player.canvas);
        canvas = player.canvas; // Keep reference to calculate bounds
        
        canvas.id = "videoCanvas";
        canvas.style.width = '100%';
        canvas.style.height = '100%';
        canvas.style.objectFit = 'contain';
        canvas.style.touchAction = 'none'; // Essential: prevent scroll/zoom defaults
        
    } catch (e) {
        console.warn("Broadway.js Player could not be initialized instantly. Ensure Decoder.js and Player.js are included.");
    }

    // -----------------------------------------------------------------
    // 2. WEBSOCKET CONNECTION
    // -----------------------------------------------------------------
    const protocol = window.location.protocol === 'https:' ? 'wss:' : 'ws:';
    const wsUrl = `${protocol}//${window.location.host}/ws`;
    const ws = new WebSocket(wsUrl);
    
    // Crucial for zero-copy binary passthrough!
    ws.binaryType = 'arraybuffer';

    ws.onopen = () => {
        console.log("Connected to Android Auto Bridge");
        // We wait for the first frame to hide the loader UI
    };

    let firstFrameReceived = false;

    ws.onmessage = (event) => {
        if (event.data instanceof ArrayBuffer) {
            // Received a raw H.264 NAL Unit frame from Rust backend
            let data = new Uint8Array(event.data);
            if (player) {
                player.decode(data);
                
                if (!firstFrameReceived) {
                    loader.classList.add("hidden");
                    firstFrameReceived = true;
                }
            }
        }
    };

    ws.onerror = (err) => console.error("WebSocket Error: ", err);
    ws.onclose = () => {
        console.log("WebSocket Disconnected");
        loader.classList.remove("hidden");
        loader.querySelector('p').innerText = 'Connection Lost. Retrying...';
        // Implement auto-reconnect if needed
    };

    // -----------------------------------------------------------------
    // 3. TOUCH COORDINATE MAPPING LOGIC (CRITICAL PATH)
    // -----------------------------------------------------------------
    
    // Action IDs representing Android Auto protocol actions
    const ACTION_DOWN = 0;
    const ACTION_UP = 1;
    const ACTION_MOVE = 2;

    const sendTouch = (action, clientX, clientY) => {
        if (ws.readyState !== WebSocket.OPEN) return;

        // getBoundingClientRect ensures we know EXACTLY how large the CSS box is
        const rect = canvas.getBoundingClientRect();
        
        // CSS Box dimensions (e.g. 1024x768 browser window size)
        const cssWidth = rect.width;
        const cssHeight = rect.height;
        
        // Video aspect ratio vs CSS aspect ratio
        const videoRatio = AA_WIDTH / AA_HEIGHT;
        const cssRatio = cssWidth / cssHeight;

        let displayedWidth = cssWidth;
        let displayedHeight = cssHeight;
        let offsetX = 0;
        let offsetY = 0;

        if (cssRatio > videoRatio) {
            // Layer is wider than video (pillarboxing: black bars on L/R)
            displayedWidth = cssHeight * videoRatio;
            offsetX = (cssWidth - displayedWidth) / 2;
        } else {
            // Layer is taller than video (letterboxing: black bars on T/B)
            displayedHeight = cssWidth / videoRatio;
            offsetY = (cssHeight - displayedHeight) / 2;
        }

        // Adjust client coordinates relative to the actual video stream box, ignoring margins
        const relX = clientX - rect.left - offsetX;
        const relY = clientY - rect.top - offsetY;

        // Check if touch is out of bounds (in the black bar areas)
        if (relX < 0 || relX > displayedWidth || relY < 0 || relY > displayedHeight) {
            return; // Ignore touches completely outside the active video
        }

        // Map relative CSS coordinates to absolute video coordinates (1920x1080)
        let mappedX = Math.round((relX / displayedWidth) * AA_WIDTH);
        let mappedY = Math.round((relY / displayedHeight) * AA_HEIGHT);

        // Clamp just in case due to rounding float issues
        mappedX = Math.max(0, Math.min(AA_WIDTH, mappedX));
        mappedY = Math.max(0, Math.min(AA_HEIGHT, mappedY));

        // Transmit JSON to Rust Axum Server
        ws.send(JSON.stringify({
            action: action,
            x: mappedX,
            y: mappedY
        }));
    };

    // -----------------------------------------------------------------
    // 4. ATTACH POINTER EVENTS (SUPPORTS MOUSE & TOUCH NATURALLY)
    // -----------------------------------------------------------------
    
    window.addEventListener('pointerdown', (e) => {
        if (e.target.id === "videoCanvas") {
            // Capture pointer so 'move' works reliably even if cursor leaves window slightly
            canvas.setPointerCapture(e.pointerId);
            sendTouch(ACTION_DOWN, e.clientX, e.clientY);
        }
    });

    window.addEventListener('pointermove', (e) => {
        // e.buttons > 0 ensures they are actively pressing/swiping
        if (e.target.id === "videoCanvas" && e.buttons > 0) {
            sendTouch(ACTION_MOVE, e.clientX, e.clientY);
            e.preventDefault(); 
        }
    }, { passive: false }); // Needs false passive to allow preventDefault on mobile

    window.addEventListener('pointerup', (e) => {
        if (canvas.hasPointerCapture(e.pointerId)) {
            canvas.releasePointerCapture(e.pointerId);
            sendTouch(ACTION_UP, e.clientX, e.clientY);
        }
    });
    
    // Prevent context menu (right click) that interrupts UI sweeps
    window.addEventListener('contextmenu', e => e.preventDefault());
});
